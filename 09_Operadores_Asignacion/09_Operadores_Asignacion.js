let a = 1;

document.writeln('Valor de a: ', a);
document.writeln('<br>');

a++; //Aumenta el valor en 1
document.writeln('Valor de a: ', a);
document.writeln('<br>');

a+=5; //Este operador aumeta el valor de la variable  dependiendo de lo que se vaya a sumar
document.writeln('Valor de a: ', a);
document.writeln('<br>');

a--; //Este operador resta el alor en 1
document.writeln('Valor de a: ', a);
document.writeln('<br>');

a-=2 //Este operador resta el valor de la variable dependiendo de lo que quiera restar
document.writeln('Valor de a: ', a);
document.writeln('<br>');

a*=2 //Este operador multiplica la variabl edependiendo de lo que quiera multiplicar
document.writeln('Valor de a: ', a);
document.writeln('<br>');

a/=4; //Este operador divide la variable dependiendo
document.writeln('Valor de a: ', a);
document.writeln('<br>');

a**=3 //Este operador potecia el valor de la variable 
document.writeln('Valor de a: ', a);
document.writeln('<br>');