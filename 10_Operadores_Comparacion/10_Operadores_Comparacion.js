/* Operador de comparacion

sirvenpara comparar contidades o valores entre variables.

Asignacion =
Mayor que >
menor que <
Igual que ==
difernecia de !=
menor o igual a <=
mayor o ifual que >=
 */

let valor1, valor2;
valor1= 20
valor2= 10;

// operador mayor que >
document.writeln(valor1 > valor2)
document.writeln('<br>'); // el sitema verifica que la variable valor1 es mayor que valor2 e imprime unresultado booleano en el DOM

// operador menor que <
document.writeln(valor1 < valor2)
document.writeln('<br>'); // el sitema verifica que la variable valor1 es mayor que valor2 e imprime unresultado booleano en el DOM

// operador igual que que ==
document.writeln(valor1 == valor2)
document.writeln('<br>'); // el sitema verifica que la variable valor1 es mayor que valor2 e imprime unresultado booleano en el DOM

// operador diferente que !=
document.writeln(valor1 != valor2)
document.writeln('<br>'); // el sitema verifica que la variable valor1 es mayor que valor2 e imprime unresultado booleano en el DOM

// operador menor o igual que <=
document.writeln(valor1 <= valor2)
document.writeln('<br>'); // el sitema verifica que la variable valor1 es mayor que valor2 e imprime unresultado booleano en el DOM

// operador mayor o igual que >=
document.writeln(valor1 >= valor2)
document.writeln('<br>'); // el sitema verifica que la variable valor1 es mayor que valor2 e imprime unresultado booleano en el DOM


